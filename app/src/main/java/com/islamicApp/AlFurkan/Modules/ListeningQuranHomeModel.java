package com.islamicApp.AlFurkan.Modules;

public class ListeningQuranHomeModel {
    String suraName, reciterName, suraLink;
    int suraPosition, reciterPosition;

    public String getSuraName() {
        return suraName;
    }

    public void setSuraName(String suraName) {
        this.suraName = suraName;
    }

    public String getReciterName() {
        return reciterName;
    }

    public void setReciterName(String reciterName) {
        this.reciterName = reciterName;
    }

    public String getSuraLink() {
        return suraLink;
    }

    public void setSuraLink(String suraLink) {
        this.suraLink = suraLink;
    }

    public int getSuraPosition() {
        return suraPosition;
    }

    public void setSuraPosition(int suraPosition) {
        this.suraPosition = suraPosition;
    }

    public int getReciterPosition() {
        return reciterPosition;
    }

    public void setReciterPosition(int reciterPosition) {
        this.reciterPosition = reciterPosition;
    }
}
