package com.islamicApp.AlFurkan.Modules;

public class RecitersItem{

	private int raw_id;

	private String server;

	private String letter;

	private String name;

	private String count;

	private String suras;

	private String rewaya;

	private String id;

	public int getRaw_id() {
		return raw_id;
	}

	public void setRaw_id(int raw_id) {
		this.raw_id = raw_id;
	}

	public void setServer(String server){
		this.server = server;
	}

	public String getServer(){
		return server;
	}

	public void setLetter(String letter){
		this.letter = letter;
	}

	public String getLetter(){
		return letter;
	}

	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return name;
	}

	public void setCount(String count){
		this.count = count;
	}

	public String getCount(){
		return count;
	}

	public void setSuras(String suras){
		this.suras = suras;
	}

	public String getSuras(){
		return suras;
	}

	public void setRewaya(String rewaya){
		this.rewaya = rewaya;
	}

	public String getRewaya(){
		return rewaya;
	}

	public void setId(String id){
		this.id = id;
	}

	public String getId(){
		return id;
	}
}